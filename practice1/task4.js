// Задание 4
// Напишите программу, которая выводит числа от 1 до 10 с помощью различных 
// циклов: while, do…while, for.

let i = 1;
while (i <= 10) {
    console.log(i);
    i++;
}

i = 1;
do {
    console.log(i);
    i++;
} while (i <= 10);

for (let i = 1; i <= 10; i++) {
    console.log(i);
}
