let user1 = {
    name: "Иван",
    age: 25,
    city: "Москва"
};

console.log("Первый пользователь:", user1);

user1.age = 26;
user1.job = "Разработчик";

delete user1.city;

console.log("Обновленный первый пользователь:", user1);

let user2 = {
    name: "Мария",
    age: 30,
    city: "Санкт-Петербург"
};

let sumOfAges = user1.age + user2.age;
